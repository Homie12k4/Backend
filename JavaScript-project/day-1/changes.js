// This file demonstrates various operations or modifications in JavaScript.

// Example of a variable change
let number = 10;
console.log("Initial number:", number);

// Changing the value of the variable
number = 20;
console.log("Changed number:", number);

// Example of an array modification
let fruits = ["apple", "banana", "cherry"];
console.log("Initial fruits:", fruits);

// Adding a new fruit
fruits.push("date");
console.log("Fruits after addition:", fruits);

// Removing a fruit
fruits.splice(1, 1); // Removes "banana"
console.log("Fruits after removal:", fruits);

// Example of an object modification
let person = {
    name: "John",
    age: 30
};
console.log("Initial person:", person);

// Changing the age property
person.age = 31;
console.log("Updated person:", person);